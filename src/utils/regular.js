/* 工具函数 */

// 正则表达式
export const userReg = () => /^[\w\u4e00-\u9fa5]{2,12}$/;  // 用户名正则，2到12位, 汉字字母数字下划线组成
export const userNameReg = () => /^[a-zA-Z0-9_-]{4,16}$/;  // 正则，4到16位（字母，数字，下划线，减号）
export const pwdReg = () => /^(?=.*[a-z])(?=.*\d)[a-zA-Z\d]{6,12}$/;  //密码强度正则，6-12个字符，至少1个大写字母，1个小写字母和1个数字     --- (?=.*[A-Z])
